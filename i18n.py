def t(key: str, lang: str = "fr", **kwargs) -> str:
    """Get translated string. Falls back to FR, then to key itself."""
    text = STRINGS.get(lang, STRINGS["fr"]).get(key, STRINGS["fr"].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text


def get_lang(request) -> str:
    """Get language from ?lang= parameter, cookie, or Accept-Language header."""
    # 1. URL param
    lang = request.query_params.get("lang")
    if lang in ("fr", "en"):
        return lang
    # 2. Cookie
    lang = request.cookies.get("glance_lang")
    if lang in ("fr", "en"):
        return lang
    # 3. Accept-Language header
    accept = request.headers.get("accept-language", "")
    if accept.startswith("en"):
        return "en"
    return "fr"
